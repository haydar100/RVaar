DROP TABLE IF EXISTS TIPSTRICKS;
DROP TABLE IF EXISTS TIPSTRICKS_CATEGORIE;
DROP TABLE IF EXISTS TRACKINGDATA;

CREATE TABLE TIPSTRICKS_CATEGORIE (
	ID INT NOT NULL AUTO_INCREMENT,
	OMSCHRIJVING VARCHAR(1000) NOT NULL,
	CONSTRAINT PK_TIPSTRICKS_CATEGORIE PRIMARY KEY(ID)
);

CREATE TABLE TIPSTRICKS (
	ID INT NOT NULL AUTO_INCREMENT,
	OMSCHRIJVING VARCHAR(1000) NOT NULL,
	GEBRUIKER_NIVEAU INT NOT NULL,
	CATEGORIE INT NOT NULL,
	CONSTRAINT PK_TIPSTRICKS PRIMARY KEY(ID),
	CONSTRAINT FK_TIPSTRICKS_CATEGORIE FOREIGN KEY(CATEGORIE) REFERENCES TIPSTRICKS_CATEGORIE(ID)
);

