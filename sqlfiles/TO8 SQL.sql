CREATE TABLE TRACKINGDATA (
	ID VARCHAR(1000) NOT NULL,
	X FLOAT NOT NULL,
	Y FLOAT NOT NULL,
	BOOTNAAM VARCHAR(100),
	LAATST_BIJGEWERKT DATETIME NOT NULL,
	RICHTING FLOAT NOT NULL,
	BOOTTYPE VARCHAR(100) NOT NULL,
	NIVEAU INT NOT NULL,
	CONSTRAINT PK_TRACKINGDATA PRIMARY KEY(ID)
	CONSTRAINT UNIEK_TRACKINGDATA UNIQUE (BOOTNAAM)
);



